import * as cheerio from "cheerio";
import fs from "node:fs/promises";
import path from "node:path";

const ROOT = new URL("../", import.meta.url);
const DATA_DIR = new URL("../data/", import.meta.url);
const SRC_DATA_DIR = new URL("../src/data/", import.meta.url);

const SOURCES = {
  ftse100: "https://en.wikipedia.org/wiki/FTSE_100_Index",
  sp500: "https://en.wikipedia.org/wiki/List_of_S%26P_500_companies"
};

function cleanText(value) {
  return String(value || "")
    .replace(/\[[^\]]+\]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function slugify(value) {
  return cleanText(value)
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function tableRows($, table) {
  const rows = [];
  $(table)
    .find("tr")
    .each((_, row) => {
      const cells = $(row)
        .find("th,td")
        .map((__, cell) => cleanText($(cell).text()))
        .get();
      if (cells.some(Boolean)) rows.push(cells);
    });
  return rows;
}

function parseFtse100(html) {
  const $ = cheerio.load(html);
  let rows = [];
  $("table.wikitable").each((_, table) => {
    const candidate = tableRows($, table);
    const header = candidate[0]?.map((cell) => cell.toLowerCase()) || [];
    if (header.includes("company") && header.includes("ticker")) rows = candidate.slice(1);
  });

  return rows
    .map((row) => ({
      id: slugify(row[0]),
      company: cleanText(row[0]),
      ticker: cleanText(row[1]).replace(/\./g, ""),
      index: "FTSE100",
      sector: cleanText(row[2]) || "Unknown",
      currency: "GBP",
      source: SOURCES.ftse100
    }))
    .filter((item) => item.company && item.ticker)
    .sort((a, b) => a.company.localeCompare(b.company));
}

function parseSp500(html) {
  const $ = cheerio.load(html);
  let rows = [];
  $("table.wikitable").each((_, table) => {
    const candidate = tableRows($, table);
    const header = candidate[0]?.map((cell) => cell.toLowerCase()) || [];
    if (header.includes("symbol") && header.includes("security") && header.some((cell) => cell.includes("cik"))) rows = candidate.slice(1);
  });

  return rows
    .map((row) => ({
      id: slugify(row[1]),
      company: cleanText(row[1]),
      ticker: cleanText(row[0]).replace(".", "-"),
      cik: cleanText(row[6]).padStart(10, "0"),
      index: "SP500",
      sector: cleanText(row[2]) || "Unknown",
      subIndustry: cleanText(row[3]) || "Unknown",
      headquarters: cleanText(row[4]) || null,
      dateAdded: cleanText(row[5]) || null,
      founded: cleanText(row[7]) || null,
      currency: "USD",
      source: SOURCES.sp500
    }))
    .filter((item) => item.company && item.ticker)
    .sort((a, b) => a.company.localeCompare(b.company));
}

async function fetchHtml(url) {
  const response = await fetch(url, {
    headers: {
      "User-Agent": "Remi remuneration dashboard constituent updater contact@remi.local",
      Accept: "text/html"
    }
  });
  if (!response.ok) throw new Error(`Failed to fetch ${url}: ${response.status}`);
  return response.text();
}

async function writeJson(fileUrl, value) {
  await fs.mkdir(new URL("./", fileUrl), { recursive: true });
  await fs.writeFile(fileUrl, `${JSON.stringify(value, null, 2)}\n`);
}

async function main() {
  await fs.mkdir(DATA_DIR, { recursive: true });
  const [ftseHtml, spHtml] = await Promise.all([fetchHtml(SOURCES.ftse100), fetchHtml(SOURCES.sp500)]);
  const ftse100 = parseFtse100(ftseHtml);
  const sp500 = parseSp500(spHtml);

  if (ftse100.length < 95) throw new Error(`FTSE 100 parse produced only ${ftse100.length} rows.`);
  if (sp500.length < 500) throw new Error(`S&P 500 parse produced only ${sp500.length} rows.`);

  await writeJson(new URL("ftse100-constituents.json", DATA_DIR), {
    source: SOURCES.ftse100,
    fetchedAt: new Date().toISOString(),
    count: ftse100.length,
    constituents: ftse100
  });
  await writeJson(new URL("sp500-constituents.json", DATA_DIR), {
    source: SOURCES.sp500,
    fetchedAt: new Date().toISOString(),
    count: sp500.length,
    constituents: sp500
  });

  const generated = `// Generated by scripts/update-constituents.mjs from ${path.relative(ROOT.pathname, DATA_DIR.pathname)}*.json.\n` +
    `export const ftse100Constituents = ${JSON.stringify(ftse100, null, 2)};\n\n` +
    `export const sp500Constituents = ${JSON.stringify(sp500, null, 2)};\n`;
  await fs.writeFile(new URL("constituents.generated.js", SRC_DATA_DIR), generated);

  console.log(`Wrote ${ftse100.length} FTSE 100 and ${sp500.length} S&P 500 constituents.`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
