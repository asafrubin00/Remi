export function buildVerifiedFtseRecord(seed) {
  const year = seed.year || VERIFIED_YEAR;
  const currency = seed.currency || "GBP";
  const sayOnPayPct = seed.sayOnPayPct ?? null;
  const executiveDirectors = (seed.directors || []).map((director) => ({
    id: `${seed.id}-${slugify(director.name)}`,
    name: director.name,
    role: director.role,
    type: "executive",
    source: "manual",
    sourceUrl: director.sourceUrl || seed.sourceUrl || null,
    payRatio: director.payRatio,
    sayOnPayPct,
    lastUpdated: VERIFIED_LAST_UPDATED,
    years: {
      [year]: {
        baseSalary: director.baseSalary,
        annualBonus: director.annualBonus,
        ltip: director.ltip,
        pensionBenefits: director.pensionBenefits,
        totalCompensation: director.totalCompensation,
        payRatio: director.payRatio,
        sayOnPayPct,
        source: "manual",
        sourceUrl: director.sourceUrl || seed.sourceUrl || null,
        lastUpdated: VERIFIED_LAST_UPDATED
      }
    }
  }));

  const nonExecutiveDirectors = (seed.nonExecutiveDirectors || []).map((director) => {
    const totalFees = director.totalFees ?? director.totalCompensation ?? null;
    return {
      id: `${seed.id}-${slugify(director.name)}`,
      name: director.name,
      role: director.role,
      type: "non-executive",
      source: "manual",
      sourceUrl: director.sourceUrl || seed.sourceUrl || null,
      payRatio: null,
      sayOnPayPct,
      lastUpdated: VERIFIED_LAST_UPDATED,
      years: {
        [year]: {
          baseSalary: null,
          annualBonus: null,
          ltip: null,
          pensionBenefits: null,
          nedFees: totalFees,
          totalCompensation: totalFees,
          payRatio: null,
          sayOnPayPct,
          source: "manual",
          sourceUrl: director.sourceUrl || seed.sourceUrl || null,
          lastUpdated: VERIFIED_LAST_UPDATED
        }
      }
    };
  });

  return {
    id: seed.id,
    company: seed.company,
    index: seed.index,
    sector: seed.sector,
    marketCap: seed.marketCap,
    currency,
    fxRate: FX_RATE,
    source: "manual",
    directors: [...executiveDirectors, ...nonExecutiveDirectors],
    scrape: {
      status: "verified",
      source: "manual",
      jurisdiction: "UK",
      message: seed.manualNote || "Manual verified remuneration data from the 2024 published annual report."
    },
    cacheTtlHours: 24,
    lastUpdated: VERIFIED_LAST_UPDATED
  };
}

export function getVerifiedFtseRecord(idOrCompany) {
  const normalized = normalize(idOrCompany);
  const seed = Object.values(manualVerifiedFtse).find((item) => {
    const normalizedId = normalize(item.id);
    const normalizedCompany = normalize(item.company);
    return normalizedId === normalized || normalizedCompany === normalized || normalizedCompany.includes(normalized);
  });
  return seed ? buildVerifiedFtseRecord(seed) : null;
}

function normalize(value) {
  return String(value || "").toLowerCase().replace(/&/g, "and").replace(/[^a-z0-9]+/g, " ").trim();
}

function slugify(value) {
  return normalize(value).replace(/\s+/g, "-");
}
